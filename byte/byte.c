#include<stdio.h>
#include<stdint.h>


int main(){
	int a = 5;//000000000 00000000 00000000 00000101
  int b = 7;//00000000 00000000 00000000 00000111
	/*
	 * 10000000 000000000 00000000 00000010 源码
	 * 11111111 111111111 11111111 11111101 反码(符号位不变，其余取反)
	 * 11111111 111111111 11111111 11111110 补码(在反码的基础上加1)
	 */

	int c = -2;
	int d = 8;

	// |（或） 运算
	/*
	 * (1) 00000000 00000000 000000000 00000101
	 *     00000000 00000000 000000000 00000111
	 *  =  00000000 00000000 000000000 00000111 7
	 */
	printf("a | b %d \n",a | b);

	/*
	 * (1) 00000000 00000000 00000000 00000101
	 *     11111111 11111111 11111111 11111110
	 *  =  11111111 11111111 11111111 11111111(补码)
	 *（2）11111111 11111111 11111111 11111110（反码）
   * (3) 10000000 00000000 00000000 00000001 (源码 -1)
	 */
	printf("a | c %d \n",a | c);

	// &（于） 运算
	/*
	 * (1) 00000000 00000000 000000000 00000101
	 *     00000000 00000000 000000000 00000111
	 *  =  00000000 00000000 000000000 00000111 7
	 */
	printf("a & b %d \n",a & b);

	//异或运算^

	/*
	 * (1) 00000000 00000000 00000000 00000101
	 *     11111111 11111111 11111111 11111110
	 *  =  11111111 11111111 11111111 11111011 (补码)
	 *（2）11111111 11111111 11111111 11111010（反码）
   * (3) 10000000 00000000 00000000 00000101 (源码 -5)
	 */
	printf("a^b %d \n",a ^ c);

	//左右移运算<<,>>
	/*	
	 * (1) 00000000 00000000 00000000 00000101
	 *  =  00000000 00000000 00000000 00010100 20
	 * (2) 00000000 00000000 00000000 00000101
	 *  =  00000000 00000000 00000000 00010001 1
	 */
	printf("a<<2 %d \n",a << 2);
	printf("a>>2 %d \n",a >> 2);

}
